// Create a variable whose value holds a string.
const stringValue = "Hello, World!";

// Create a variable whose value holds a number.
const numberValue = 42;

// Create a variable whose value holds an object.
const objectValue = { key: "value" };

// Create a variable whose value holds a boolean.
const booleanValue = true;

